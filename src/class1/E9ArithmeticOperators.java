package class1;

public class E9ArithmeticOperators {

    public static void main(String[] args) {

        double num1=1.5;
        double num2=2.5;
        System.out.println(num1+num2);
        System.out.println(num2-num1);
        System.out.println(num1*num2);
        System.out.println(num2/num1);
    }
}

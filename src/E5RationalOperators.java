public class E5RationalOperators {

    public static void main(String[] args) {
        System.out.println(10>15);
        System.out.println(10<15);
        System.out.println(10==15);
        System.out.println(10!=15);
        System.out.println(10>=15);
        System.out.println(10<=15);
    }
}
